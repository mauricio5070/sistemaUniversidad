CREATE TABLE rol (
  id int unsigned NOT NULL AUTO_INCREMENT,
  idRol VARCHAR(15) NOT NULL,
  descripcionRol VARCHAR(30) NOT NULL,
  plantilla VARCHAR(15) NOT NULL,
  PRIMARY KEY (id),
  unique key  `idRol` (idRol)
);

CREATE TABLE usuario (
  id int unsigned NOT NULL AUTO_INCREMENT,
  idUsuario VARCHAR(10) NOT NULL,
  pwUsuario varchar(255) not null,
  nombreUsuario VARCHAR(30) NOT NULL,
  idRol VARCHAR(15) NOT NULL,
  PRIMARY KEY (id),
  unique key  `idUsuario` (idUsuario),  
  CONSTRAINT FK_Usuario_Rol
    FOREIGN KEY (idRol)
    REFERENCES rol(idRol)
);

create table estudiante (
  id int unsigned NOT NULL AUTO_INCREMENT,
  idEstudiante varchar(10),
  nombre varchar(20) not null,
  apellido1 varchar(15) not null,
  apellido2 varchar(15) not null,
  fechaNaci date not null,
  fechaIngr date not null,
  genero varchar(1),
  PRIMARY KEY (id),
  unique key  `idEstudiante` (idEstudiante)
);

create table materia(
   id int unsigned NOT NULL AUTO_INCREMENT,
   idMateria varchar(10) not null,
   nombre varchar(70) not null,
   PRIMARY KEY (id),
   unique key  `idMateria` (idMateria)
);

create table periodo (
  id int unsigned NOT NULL AUTO_INCREMENT,
  idPeriodo varchar (5) not null,
  fechaInicio Date,
  fechaFin Date,
  PRIMARY KEY (id),
  unique key  `idPeriodo` (idPeriodo)
);

create table notas (
  id int unsigned NOT NULL AUTO_INCREMENT,
  idEstudiante varchar(10) not null, 
  idMateria varchar(10) not null,
  idPeriodo varchar(5) not null,
  nota double, 
  PRIMARY KEY (id),
  unique key  `llave` (idEstudiante,idMateria,idPeriodo),
  foreign key (idEstudiante) references estudiante(idEstudiante),
  foreign key (idMateria) references materia(idMateria),
  foreign key (idPeriodo) references periodo(idPeriodo)
);

create table prospecto (
  id int unsigned NOT NULL AUTO_INCREMENT,
  idProspecto varchar(10) not null,
  nombre varchar(20) not null,
  Apellido1 varchar(15) not null,
  Apellido2 varchar(15) not null,
